package Day1;

public class CharDemo {

	public static void main(String[] args) {
		char a='a';
		System.out.println(a);
		char ch1=65;
		System.out.println(ch1);
		char var1 = '\u00A7';
		System.out.println(var1);
		char var2 = '\u20AC';
		System.out.println(var2);
		int b = 'B';
		System.out.println(b);
		char vv='\u00c4';
		System.out.println(vv);
		

	}

}
